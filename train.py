# ─────────────────────────────────────────────────────────────────────────────
# Device selection — M1 Mac uses MPS
# ─────────────────────────────────────────────────────────────────────────────

def get_device() -> torch.device:
    if torch.backends.mps.is_available():
        print("Using Apple MPS (M1 GPU)")
        return torch.device('mps')
    elif torch.cuda.is_available():
        print(f"Using CUDA: {torch.cuda.get_device_name(0)}")
        return torch.device('cuda')
    else:
        print("Using CPU")
        return torch.device('cpu')


# ─────────────────────────────────────────────────────────────────────────────
# Dataset wrapper
# ─────────────────────────────────────────────────────────────────────────────

class FisherDataset(Dataset):
    """Loads the HDF5 dataset generated by dataset.py"""

    def __init__(self, h5_path: str):
        self.h5_path = h5_path
        with h5py.File(h5_path, 'r') as f:
            self.n = f['images'].shape[0]
            # load everything into RAM for fast M1 access
            self.images      = torch.tensor(f['images'][:],      dtype=torch.float32)
            self.cos_coeffs  = torch.tensor(f['cos_coeffs'][:],  dtype=torch.float32)
            self.sin_coeffs  = torch.tensor(f['sin_coeffs'][:],  dtype=torch.float32)
            self.AF          = torch.tensor(f['AF'][:],           dtype=torch.float32)
            self.descriptors = torch.tensor(f['descriptor'][:],   dtype=torch.float32)
            self.geom_ids    = torch.tensor(f['geometry_id'][:],  dtype=torch.long)
            self.snr_db      = torch.tensor(f['snr_db'][:],       dtype=torch.float32)
            self.sigma_b     = torch.tensor(f['sigma_b'][:],      dtype=torch.float32)

    def __len__(self):
        return self.n

    def __getitem__(self, idx):
        return {
            'image':      self.images[idx],
            'descriptor': self.descriptors[idx],
            'cos':        self.cos_coeffs[idx],
            'sin':        self.sin_coeffs[idx],
            'AF':         self.AF[idx],
            'geom_id':    self.geom_ids[idx],
            'snr_db':     self.snr_db[idx],
            'sigma_b':    self.sigma_b[idx],
        }


# ─────────────────────────────────────────────────────────────────────────────
# Loss function
# ─────────────────────────────────────────────────────────────────────────────

class FisherLoss(nn.Module):
    """
    L = λ_F · MSE(Fourier) + λ_A · MSE(A_F) + λ_C · L_consistency

    Consistency loss: penalises disagreement between predictions on
    the original and a randomly rotated version of the same image.
    (Simplified here as L2 between two forward passes with different
    augmentation seeds — full group-equivariant version can be added.)

    [Karniadakis et al. 2021]
    """
    def __init__(self, lambda_F: float = 1.0,
                        lambda_A: float = 0.5,
                        lambda_C: float = 0.1):
        super().__init__()
        self.lambda_F = lambda_F
        self.lambda_A = lambda_A
        self.lambda_C = lambda_C
        self.mse      = nn.MSELoss()

    def forward(self, cos_pred, sin_pred, AF_pred,
                cos_true, sin_true, AF_true,
                cos_pred2=None, sin_pred2=None):
        """
        Parameters
        ----------
        cos_pred, sin_pred : (B, M)  predicted Fourier coefficients
        AF_pred            : (B, 1)  predicted anisotropy index
        cos_true, sin_true : (B, M)  ground truth
        AF_true            : (B,)    ground truth
        cos_pred2, sin_pred2 : optional second pass for consistency
        """
        L_fourier = self.mse(cos_pred, cos_true) + \
                    self.mse(sin_pred, sin_true)
        # Scale A_F loss: true A_F ∈ [0,1] but Fourier coeffs can be O(1).
        # Normalise both to unit variance so neither dominates.
        L_AF      = self.mse(AF_pred.squeeze(), AF_true)

        L_cons = torch.tensor(0.0, device=cos_pred.device)
        if cos_pred2 is not None:
            L_cons = self.mse(cos_pred, cos_pred2) + \
                     self.mse(sin_pred, sin_pred2)

        total = (self.lambda_F * L_fourier +
                 self.lambda_A * L_AF +
                 self.lambda_C * L_cons)
        return total, L_fourier.item(), L_AF.item(), L_cons.item()


# ─────────────────────────────────────────────────────────────────────────────
# Training utilities
# ─────────────────────────────────────────────────────────────────────────────

def rotate_images_90(images: torch.Tensor) -> torch.Tensor:
    """Rotate batch of images by 90° for consistency regularisation."""
    return torch.rot90(images, k=1, dims=[2, 3])


def compute_metrics(model, loader, device, criterion):
    """Full evaluation on a DataLoader. Returns dict of metrics."""
    model.eval()
    all_AF_pred, all_AF_true = [], []
    all_cos_pred, all_cos_true = [], []
    total_loss = 0.0
    n_batches  = 0

    with torch.no_grad():
        for batch in loader:
            img  = batch['image'].to(device)
            desc = batch['descriptor'].to(device)
            cos_t = batch['cos'].to(device)
            sin_t = batch['sin'].to(device)
            AF_t  = batch['AF'].to(device)

            cos_p, sin_p, AF_p = model(img, desc)
            loss, _, _, _ = criterion(cos_p, sin_p, AF_p, cos_t, sin_t, AF_t)

            total_loss += loss.item()
            n_batches  += 1

            all_AF_pred.extend(AF_p.squeeze().cpu().numpy())
            all_AF_true.extend(AF_t.cpu().numpy())
            all_cos_pred.append(cos_p.cpu().numpy())
            all_cos_true.append(cos_t.cpu().numpy())

    all_AF_pred = np.array(all_AF_pred)
    all_AF_true = np.array(all_AF_true)
    all_cos_pred = np.vstack(all_cos_pred)
    all_cos_true = np.vstack(all_cos_true)

    # MSE over Fourier coefficients
    mse_fourier = float(np.mean((all_cos_pred - all_cos_true)**2))
    # Pearson r for A_F
    pearson_r = float(np.corrcoef(all_AF_pred, all_AF_true)[0, 1])
    # MAE for A_F
    mae_AF = float(np.mean(np.abs(all_AF_pred - all_AF_true)))

    return {
        'loss':        total_loss / max(n_batches, 1),
        'mse_fourier': mse_fourier,
        'pearson_r':   pearson_r,
        'mae_AF':      mae_AF,
    }


