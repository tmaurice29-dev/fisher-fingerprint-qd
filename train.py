def plot_training_curves(history: dict, out_dir: Path):
    fig, axes = plt.subplots(1, 3, figsize=(15, 4))

    axes[0].plot(history['train_loss'], label='Train')
    axes[0].plot(history['val_loss'],   label='Val')
    axes[0].set_xlabel('Epoch'); axes[0].set_ylabel('Total Loss')
    axes[0].set_title('Loss'); axes[0].legend(); axes[0].set_yscale('log')

    axes[1].plot(history['val_mse_fourier'])
    axes[1].set_xlabel('Epoch'); axes[1].set_ylabel('MSE')
    axes[1].set_title('Fourier Coefficient MSE (val)')

    axes[2].plot(history['val_pearson_r'])
    axes[2].set_xlabel('Epoch'); axes[2].set_ylabel('r')
    axes[2].set_title('Pearson r — Anisotropy Index (val)')
    axes[2].set_ylim(0, 1)

    plt.tight_layout()
    plt.savefig(out_dir / 'training_curves.png', dpi=150)
    plt.close()
    print(f"Saved training curves → {out_dir / 'training_curves.png'}")


def plot_parity(model, loader, device, out_dir: Path, n_points: int = 500):
    """Scatter plot of predicted vs true A_F."""
    model.eval()
    preds, trues = [], []
    with torch.no_grad():
        for batch in loader:
            if len(preds) >= n_points:
                break
            img  = batch['image'].to(device)
            desc = batch['descriptor'].to(device)
            AF_t = batch['AF'].numpy()
            _, _, AF_p = model(img, desc)
            preds.extend(AF_p.squeeze().cpu().numpy())
            trues.extend(AF_t)

    preds = np.array(preds[:n_points])
    trues = np.array(trues[:n_points])
    r2 = r2_score(trues, preds)

    fig, ax = plt.subplots(figsize=(5, 5))
    ax.scatter(trues, preds, s=8, alpha=0.4, c='steelblue')
    lo, hi = min(trues.min(), preds.min()), max(trues.max(), preds.max())
    ax.plot([lo, hi], [lo, hi], 'r--', lw=1.5, label='ideal')
    ax.set_xlabel('True $A_F$'); ax.set_ylabel('Predicted $A_F$')
    ax.set_title(f'Anisotropy Index Parity  ($R^2={r2:.3f}$)')
    ax.legend()
    plt.tight_layout()
    plt.savefig(out_dir / 'parity_AF.png', dpi=150)
    plt.close()
    print(f"Saved parity plot → {out_dir / 'parity_AF.png'}")


# ─────────────────────────────────────────────────────────────────────────────
# Main training function
# ─────────────────────────────────────────────────────────────────────────────

def train(args):
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    device  = get_device()

    # ── Build / load dataset ─────────────────────────────────────────────────
    ds_path = Path(args.dataset)
    if not ds_path.exists():
        print(f"Dataset not found at {ds_path}. Generating {args.n_samples} samples …")
        generate_dataset(
            n_samples=args.n_samples,
            grid_size=args.grid_size,
            output_path=str(ds_path),
            verbose=True,
        )

    print(f"Loading dataset from {ds_path} …")
    full_ds = FisherDataset(str(ds_path))
    print(f"  Total samples: {len(full_ds)}")

    # 80/10/10 split
    n_total = len(full_ds)
    n_val   = int(0.10 * n_total)
    n_test  = int(0.10 * n_total)
    n_train = n_total - n_val - n_test

    train_ds, val_ds, test_ds = random_split(
        full_ds, [n_train, n_val, n_test],
        generator=torch.Generator().manual_seed(42)
    )
    print(f"  Train: {len(train_ds)}  Val: {len(val_ds)}  Test: {len(test_ds)}")

    train_loader = DataLoader(train_ds, batch_size=args.batch_size,
                              shuffle=True,  num_workers=0, pin_memory=False)
    val_loader   = DataLoader(val_ds,   batch_size=args.batch_size,
                              shuffle=False, num_workers=0, pin_memory=False)
    test_loader  = DataLoader(test_ds,  batch_size=args.batch_size,
                              shuffle=False, num_workers=0, pin_memory=False)

    # ── Model ────────────────────────────────────────────────────────────────
    descriptor_dim = full_ds.descriptors.shape[1]  # 2K+1 = 7
    model = FisherCNN(
        grid_size=args.grid_size,
        descriptor_dim=descriptor_dim,
        M=args.M,
        dropout_rate=args.dropout,
    ).to(device)
    print(f"Model parameters: {model.count_parameters():,}")

    # ── Loss & optimizer ─────────────────────────────────────────────────────
    criterion = FisherLoss(
        lambda_F=args.lambda_F,
        lambda_A=args.lambda_A,
        lambda_C=args.lambda_C,
    )
    optimizer = torch.optim.Adam(model.parameters(), lr=args.lr)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(
        optimizer, T_max=args.epochs, eta_min=1e-6
    )

    # ── Training loop ────────────────────────────────────────────────────────
    history = {k: [] for k in
               ['train_loss','val_loss','val_mse_fourier','val_pearson_r','val_mae_AF']}
    best_val_loss = float('inf')
    patience_count = 0
    ckpt_path = out_dir / 'best_model.pt'

    print(f"\nTraining for up to {args.epochs} epochs (early stop patience={args.patience})\n")
    t0 = time.time()

    for epoch in range(1, args.epochs + 1):
        # ── Train ────────────────────────────────────────────────────────────
        model.train()
        epoch_loss = 0.0
        pbar = tqdm(train_loader,
                    desc=f"Epoch {epoch:3d}/{args.epochs}",
                    leave=False)

        for batch in pbar:
            img  = batch['image'].to(device)
            desc = batch['descriptor'].to(device)
            cos_t = batch['cos'].to(device)
            sin_t = batch['sin'].to(device)
            AF_t  = batch['AF'].to(device)

            optimizer.zero_grad()
            cos_p, sin_p, AF_p = model(img, desc)

            # consistency pass: rotate images by 90°
            img_rot   = rotate_images_90(img)
            cos_p2, sin_p2, _ = model(img_rot, desc)

            loss, lf, la, lc = criterion(
                cos_p, sin_p, AF_p,
                cos_t, sin_t, AF_t,
                cos_p2, sin_p2
            )
            loss.backward()
            nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
            optimizer.step()

            epoch_loss += loss.item()
            pbar.set_postfix({'loss': f'{loss.item():.4f}',
                              'Lf': f'{lf:.4f}',
                              'La': f'{la:.4f}'})

        scheduler.step()

        # ── Validate ──────────────────────────────────────────────────────
        val_metrics = compute_metrics(model, val_loader, device, criterion)

        history['train_loss'].append(epoch_loss / len(train_loader))
        history['val_loss'].append(val_metrics['loss'])
        history['val_mse_fourier'].append(val_metrics['mse_fourier'])
        history['val_pearson_r'].append(val_metrics['pearson_r'])
        history['val_mae_AF'].append(val_metrics['mae_AF'])

        lr_now = scheduler.get_last_lr()[0]
        print(f"Epoch {epoch:3d} | "
              f"train={history['train_loss'][-1]:.4f} | "
              f"val={val_metrics['loss']:.4f} | "
              f"MSE_F={val_metrics['mse_fourier']:.4e} | "
              f"r_AF={val_metrics['pearson_r']:.3f} | "
              f"LR={lr_now:.2e}")

        # ── Checkpoint ────────────────────────────────────────────────────
        if val_metrics['loss'] < best_val_loss:
            best_val_loss = val_metrics['loss']
            torch.save({
                'epoch':      epoch,
                'model_state': model.state_dict(),
                'optimizer':   optimizer.state_dict(),
                'val_metrics': val_metrics,
                'args':        vars(args),
            }, ckpt_path)
            patience_count = 0
        else:
            patience_count += 1
            if patience_count >= args.patience:
                print(f"\nEarly stopping at epoch {epoch} "
                      f"(no improvement for {args.patience} epochs).")
                break

    elapsed = time.time() - t0
    print(f"\nTraining complete in {elapsed/60:.1f} min.")

    # ── Final test evaluation ─────────────────────────────────────────────
    print("\nLoading best checkpoint for test evaluation …")
    ckpt = torch.load(ckpt_path, map_location=device)
    model.load_state_dict(ckpt['model_state'])
    test_metrics = compute_metrics(model, test_loader, device, criterion)

    print("\n══════ TEST RESULTS ══════")
    print(f"  Loss           : {test_metrics['loss']:.4f}")
    print(f"  MSE (Fourier)  : {test_metrics['mse_fourier']:.4e}")
    print(f"  Pearson r (AF) : {test_metrics['pearson_r']:.4f}")
    print(f"  MAE   (AF)     : {test_metrics['mae_AF']:.4f}")
    print("══════════════════════════\n")

    # ── Save outputs ──────────────────────────────────────────────────────
    results = {
        'test_metrics':  test_metrics,
        'best_val_loss': best_val_loss,
        'n_params':      model.count_parameters(),
        'epochs_trained': len(history['train_loss']),
        'elapsed_min':   elapsed / 60,
    }
    with open(out_dir / 'results.json', 'w') as f:
        json.dump(results, f, indent=2)

    plot_training_curves(history, out_dir)
    plot_parity(model, test_loader, device, out_dir)

    print(f"All outputs saved to → {out_dir}/")
    return model, test_metrics


