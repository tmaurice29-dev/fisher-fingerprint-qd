"""
train.py
========
Training loop for the FisherCNN model.

Usage
-----
    python train.py                          # train with defaults
    python train.py --n_samples 10000        # quick test run
    python train.py --dataset dataset.h5     # use pre-built dataset

Features
--------
  - Auto-detects MPS (Apple M1), CUDA, or CPU
  - Multi-task loss: λ_F · MSE_Fourier + λ_A · MSE_AF + λ_C · consistency
  - Cosine LR schedule + early stopping
  - Checkpoints best model by validation loss
  - Saves training curves and evaluation plots
"""

