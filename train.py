def parse_args():
    p = argparse.ArgumentParser(
        description='Train FisherCNN on quantum dot dataset')

    # Dataset
    p.add_argument('--dataset',    default='dataset.h5',
                   help='Path to HDF5 dataset (generated if missing)')
    p.add_argument('--n_samples',  type=int, default=80_000,
                   help='Samples to generate if dataset missing')
    p.add_argument('--grid_size',  type=int, default=64)
    p.add_argument('--M',          type=int, default=6,
                   help='Number of Fourier modes to predict')

    # Model
    p.add_argument('--dropout',    type=float, default=0.1)

    # Loss weights  [Karniadakis et al. 2021]
    p.add_argument('--lambda_F',   type=float, default=1.0)
    p.add_argument('--lambda_A',   type=float, default=0.5)
    p.add_argument('--lambda_C',   type=float, default=0.1)

    # Training
    p.add_argument('--epochs',     type=int,   default=120)
    p.add_argument('--batch_size', type=int,   default=64)
    p.add_argument('--lr',         type=float, default=1e-3)
    p.add_argument('--patience',   type=int,   default=20,
                   help='Early stopping patience (epochs)')

    # Output
    p.add_argument('--out_dir',    default='outputs/',
                   help='Directory for checkpoints and plots')

    return p.parse_args()


if __name__ == '__main__':
    args = parse_args()
    train(args)
